/**
 * 提取字符串的拼音首字母
 * 中文→拼音首字母，英文→小写，数字→原样
 */
export function getPinyinInitials(text: string): string {
  let result = '';
  for (const char of text) {
    result += PINYIN_INITIAL_MAP[char] || char.toLowerCase();
  }
  return result;
}

/**
 * 模糊匹配客商名称
 * - 直接包含匹配：输入 "深圳" 匹配 "深圳科技有限公司"
 * - 拼音首字母匹配：输入 "sz" 匹配 "深圳科技有限公司"
 */
export function matchPartnerName(input: string, partnerName: string): boolean {
  if (!input || !partnerName) return false;
  const lowerInput = input.toLowerCase().trim();
  const lowerName = partnerName.toLowerCase();

  // 直接包含匹配
  if (lowerName.includes(lowerInput)) return true;

  // 拼音首字母匹配
  const initials = getPinyinInitials(partnerName);
  if (initials.includes(lowerInput)) return true;

  return false;
}
